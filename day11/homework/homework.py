if operation == "+":
    result = num1 + num2
    print("შედეგი:", result)
elif operation == "-":
    result = num1 - num2
    print("შედეგი:", result)
elif operation == "*":
    result = num1 * num2
    print("შედეგი:", result)
elif operation == "/":
    if num2 != 0:
        result = num1 / num2
        print("შედეგი:", result)
    else:
        print("არასწორი ოპერაცია! (გაყოფა ნულზე)")
