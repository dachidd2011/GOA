# 2)შექმენით Tuple -ი და შეცვალეთ მეორე ინდექსზე მყოფი ელემენტი ზუსტად ისე როგორც გაკვეთილზე განახეთ, 
# სბოლოოდ გამოპრინტეთ ახალი tuple ელემენტ შეცვლილი. ახსენით კომენტარებით რომელ ხაზზე რა ხდება.



# Tuple-ის შექმნა
numbers = (10, 20, 30, 40, 50)

# Tuple პირდაპირ არ იცვლება, ამიტომ ვაქცევთ List-ად
temp_list = list(numbers)

# ვცვლით მეორე ინდექსზე მყოფ ელემენტს
temp_list[2] = 300

# ისევ ვაქცევთ Tuple-ად
numbers = tuple(temp_list)

# შედეგის დაბეჭდვა
print(numbers)